class Solution {
  public:
    vector<vector<int>> getPairs(vector<int>& arr) {
    std::unordered_set<int> seen;
    std::unordered_set<std::string> uniquePairs;
    std::vector<std::vector<int>> result;

    for (int num : arr) {
        if (seen.count(-num)) {
            // Form the pair in sorted order
            int a = std::min(num, -num);
            int b = std::max(num, -num);
            std::string pairKey = std::to_string(a) + "," + std::to_string(b);
            
            if (!uniquePairs.count(pairKey)) {
                uniquePairs.insert(pairKey);
                result.push_back({a, b});
            }
        }
        seen.insert(num);
    }

    // Sort the result vector to ensure sorted order
    std::sort(result.begin(), result.end());
    return result;
    }
};

