class Solution {
  public:
    string armstrongNumber(int n) {
        //Approach-1
        int sum=0;
        for(int i=n;i!=0;i=i/10){
            int dig=i%10;
            sum+=dig*dig*dig;
        }
        if(sum==n)return "true";
        return "false";
    }
};

